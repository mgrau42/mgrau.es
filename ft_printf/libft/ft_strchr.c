#include "libft.h"

char	*ft_strchr(char *str, int c)
{
	if (!(str))
		return (NULL);
	while (*str != '\0')
	{
		if (*str == c)
			return (str);
		str++;
	}
	if (*str == c)
		return (str);
	return (NULL);
}
